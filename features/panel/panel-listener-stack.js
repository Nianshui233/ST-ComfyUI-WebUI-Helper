import { createPanelApiListeners } from './panel-api-listeners.js';
import { createPanelCacheListeners } from './panel-cache-listeners.js';
import { createPanelGeneralListeners } from './panel-general-listeners.js';
import { createPanelWorkflowListeners } from './panel-workflow-listeners.js';
import { createApiImagePanelController } from '../api-image/api-image-panel-controller.js';

export function createPanelListenerStack({
    makeRequest,
    connectionMonitor,
    manualScan,
    saveSettings,
    detectAiPromptModels,
    testAiPromptOpenAICompatibleApi,
    testApiImageGeneration,
    fetchAndPopulateModels,
    fetchAndPopulateUNetModels,
    fetchAndPopulateWebUIModels,
    fetchAndPopulateWebUILoras,
    fetchAndPopulateWebUIEmbeddings,
    fetchAndPopulateComfyUISamplingOptions,
    fetchAndPopulateWebUISamplingOptions,
    fetchAndPopulateComfyUILoras,
    renderComfyUILoraList,
    clearComfyUILoraSelection,
    applyComfyUILoraBulkWeights,
    selectFilteredComfyUILoras,
    setAllComfyUILorasEnabled,
    copyComfyUILoraSelection,
    exportComfyUILoraSelection,
    importComfyUILoraSelection,
    copyLastSubmittedComfyUIWorkflow,
    exportLastSubmittedComfyUIWorkflow,
    getValue,
    setValue,
    imageCacheDB,
    blobUrlTracker,
    loadImageCache,
    clearAllCache,
    filterWorkflows,
    toggleEditMode,
    saveEditedWorkflow,
    cancelEditMode,
    formatCurrentWorkflow,
    copyCurrentWorkflow,
    minifyCurrentWorkflow,
    analyzeCurrentWorkflow,
    insertWorkflowPlaceholder,
    convertCurrentWorkflowToPlaceholders,
    validateCurrentWorkflow,
    showWorkflowSaveModal,
    exportAllWorkflows,
    importAllWorkflows,
    validateComfyWorkflow,
    showWorkflowValidationResult,
    switchMode,
    moveAdvancedSectionsToTab,
    img2imgController,
    updateSelectedEmbeddingsDisplay,
    updateComfyUISelectedLorasDisplay,
    updateWorkflowList,
    deviceDetector,
    showToast,
    logger = console,
}) {
    const { initApiListeners } = createPanelApiListeners({
        makeRequest,
        connectionMonitor,
        manualScan,
        saveSettings,
        detectAiPromptModels,
        testAiPromptOpenAICompatibleApi,
        fetchAndPopulateModels,
        fetchAndPopulateUNetModels,
        fetchAndPopulateWebUIModels,
        fetchAndPopulateWebUILoras,
        fetchAndPopulateWebUIEmbeddings,
        fetchAndPopulateComfyUISamplingOptions,
        fetchAndPopulateWebUISamplingOptions,
        fetchAndPopulateComfyUILoras,
        renderComfyUILoraList,
        clearComfyUILoraSelection,
        applyComfyUILoraBulkWeights,
        selectFilteredComfyUILoras,
        setAllComfyUILorasEnabled,
        copyComfyUILoraSelection,
        exportComfyUILoraSelection,
        importComfyUILoraSelection,
        copyLastSubmittedComfyUIWorkflow,
        exportLastSubmittedComfyUIWorkflow,
        showToast,
        logger,
    });

    const { initCacheListeners } = createPanelCacheListeners({
        getValue,
        setValue,
        imageCacheDB,
        blobUrlTracker,
        loadImageCache,
        clearAllCache,
        showToast,
        logger,
    });

    const { initWorkflowListeners } = createPanelWorkflowListeners({
        filterWorkflows,
        toggleEditMode,
        saveEditedWorkflow,
        cancelEditMode,
        formatCurrentWorkflow,
        copyCurrentWorkflow,
        minifyCurrentWorkflow,
        analyzeCurrentWorkflow,
        insertWorkflowPlaceholder,
        convertCurrentWorkflowToPlaceholders,
        validateCurrentWorkflow,
        showWorkflowSaveModal,
        exportAllWorkflows,
        importAllWorkflows,
        validateComfyWorkflow,
        showWorkflowValidationResult,
        showToast,
    });

    const {
        initGeneralListeners,
        initTabListeners,
        moveModeSections,
    } = createPanelGeneralListeners({
        getValue,
        setValue,
        saveSettings,
        detectAiPromptModels,
        switchMode,
        moveAdvancedSectionsToTab,
        img2imgController,
        updateSelectedEmbeddingsDisplay,
        updateComfyUISelectedLorasDisplay,
        loadImageCache,
        updateWorkflowList,
        deviceDetector,
        showToast,
        logger,
    });

    function initApiImageListeners(buttons, inputs) {
        const controller = createApiImagePanelController({
            buttons,
            inputs,
            getValue,
            setValue,
            saveSettings,
            testApiImageGeneration,
            showToast,
            logger,
        });
        controller.init();
        return controller;
    }

    return {
        initApiImageListeners,
        initApiListeners,
        initCacheListeners,
        initGeneralListeners,
        initTabListeners,
        initWorkflowListeners,
        moveModeSections,
    };
}
